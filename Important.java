public class Important {
    /*
    Easy memory trick
PascalCase → Every word starts with a Capital letter.
camelCase → First word small, then humps (Age, Name, Marks) like a camel's back.
UPPER_CASE → Everything capital with _. */



public class StudentInfo {      // PascalCase

    static final int MAX_AGE = 100;  // UPPER_CASE

    int studentAge = 20;       // camelCase

    void printDetails() {      // camelCase
        System.out.println(studentAge);
    }
}
}
